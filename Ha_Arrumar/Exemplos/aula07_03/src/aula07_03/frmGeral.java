    private void btnConverterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConverterActionPerformed
        
        Double valor, resultado;
        resultado = 0.1;
        try{
            valor = Double.parseDouble(txtPrimeiroValo.getText());
            if(cbPrimeiroGrau.getItemAt(cbPrimeiroGrau.getSelectedIndex())== "C")
            {
                if(cbPrimeiroGrau.getItemAt(cbPrimeiroGrau.getSelectedIndex())== "F")
                {
                    resultado = valor * 1.8 + 32;
                }
                if(cbPrimeiroGrau.getItemAt(cbPrimeiroGrau.getSelectedIndex()) == "K")
                {
                    resultado = valor +273;
                }
            }
            if(cbPrimeiroGrau.getItemAt(cbPrimeiroGrau.getSelectedIndex()) == "F")
            {
                if(cbPrimeiroGrau.getItemAt(cbPrimeiroGrau.getSelectedIndex())== "C")
                {
                    resultado = (valor - 32)/ 1.8 ;
                }
                if(cbPrimeiroGrau.getItemAt(cbPrimeiroGrau.getSelectedIndex())== "K")
                {
                    resultado = (valor - 32)/ 1.8 + 273;
                }
                resultado = 0.0;
            }
            if(cbPrimeiroGrau.getItemAt(cbPrimeiroGrau.getSelectedIndex())== "K")
            {
                if(cbPrimeiroGrau.getItemAt(cbPrimeiroGrau.getSelectedIndex())== "C")
                {
                    resultado = valor - 273;
                }
                if(cbPrimeiroGrau.getItemAt(cbPrimeiroGrau.getSelectedIndex())== "F")
                {
                    resultado = (valor - 273)*1.8 + 32;
                }
            }
            txtSegundoValor.setText(""+resultado);
        }catch (Exception ex)
    {
        
    }
    }//GEN-LAST:event_btnConverterActionPerformed

